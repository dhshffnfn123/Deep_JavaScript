// 함수는 함수이름으로 호출하는 것이 아니라 함수 객체를 가리키는 식별자로 호출한다.

/* --------------------------------- 함수 표현식 --------------------------------- */
// 자바스크립트의 함수는 값처럼 변수에 할당할 수도 있고 프로퍼티 값이 될 수도 있으며 배열의 요소가 될 수도 있다.
// 이처럼 값의 성질을 갖는 객체를 일급 객체라 한다. 따라서 자바스크립트의 함수는 일급객체이다.
// 함수 표현식의 함수이름은 생략하는 것이 일반적이다.


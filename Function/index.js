// 함수 이름은 몸체 내부에서만 유효한 식별자다.
// console.log(foo(2,5)); // RE: foo is not defined

// 함수 선언문은 '표현식이 아닌 문'이고 함수 표현식은 '표현식인 문'이다.

// 함수 생성 시점과 함수 호이스팅
// 함수 참조

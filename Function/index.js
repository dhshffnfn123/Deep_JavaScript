// 인수 1과 2가 매개변수 x와 y에 순서대로 할당되고 함수 몸체의 문들이 실행된다.
var result = add(1, 2);

// 함수가 호출되면 함수 몸체 내에서 매개변수가 암묵적으로 생성되고 일반 변수와 마찬가지로 undefined로 초기화 된 후 인수를 순서대로 할당한다.
// 매개변수는 함수 몸체 내부에서만 참조할 수 있다. 즉, 매개변수의 스코프(유효범위)는 함수 내부다.
